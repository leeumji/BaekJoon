package For;

import java.util.Scanner;

public class Main2742 {
    public static void main(String[] args) {
        Scanner scr = new Scanner(System.in);
        int a = scr.nextInt();

        for (int i = a; i >= 1; i--) {
            System.out.println(i);
        }
        System.out.println();
    }
}
